# Prompt the user for their current age

current_age = int(input("How old are you? "))

# Calculate the number of years from 2023 to 2050

years_until_2050 = 2050 - 2023

# Calculate the future age

age_in_2050 = current_age + years_until_2050

# Print the result

print(f"In 2050, you will be {age_in_2050} years old.")
